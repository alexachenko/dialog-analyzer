def generate_dialog(topic, data):
    client = random.choice(data["client"])
    operator = random.choice(data["operator"])
    client = client.replace("{days}", str(random.randint(3, 30)))
    client = client.replace("{hours}", str(random.randint(2, 6)))
    return f"Клиент: {client} Оператор: {operator}"

all_dialogs = []
for topic, data in topics_data.items():
    for i in range(500):
        all_dialogs.append([generate_dialog(topic, data), topic])
    print(f"Сгенерировано 500 для темы: {topic}")

with open("training_data_topics.csv", "w", newline="", encoding="utf-8") as f:
    writer = csv.writer(f)
    writer.writerow(["dialog", "topic"])
    writer.writerows(all_dialogs)

print(f"\nВсего: {len(all_dialogs)} диалогов для тем")