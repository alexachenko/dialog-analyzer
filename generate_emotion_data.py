import random
import csv

# ============ ТОЛЬКО ЯВНЫЙ ПОЗИТИВ (ВОСТОРГ, ПОХВАЛА) ============
positive_words = [
    "супер", "восторг", "вау", "отлично", "прекрасно", "замечательно", 
    "круто", "класс", "потрясающе", "шикарно", "великолепно", "бомба",
    "вы лучшие", "вы супер", "обожаю", "в восторге", "блестяще", "чудесно"
]

# ============ ТОЛЬКО ЯВНЫЙ НЕГАТИВ (ПРЕТЕНЗИИ, РУГАНЬ) ============
negative_words = [
    "недопустимо", "ужасно", "кошмар", "безобразие", "возмутительно", 
    "отвратительно", "позор", "катастрофа", "провал", "хамство",
    "никогда больше", "жалоба", "претензия", "скандал", "в ярости"
]

# ============ НЕЙТРАЛЬНЫЕ (СПАСИБО, ХОРОШО, ОКЕЙ — ЭТО НЕ ПОЗИТИВ) ============
neutral_words = [
    "спасибо", "хорошо", "окей", "понял", "договорились", "ждите", 
    "сделайте", "пожалуйста", "принято", "ладно", "ясно", "проверьте",
    "оформите", "верните", "жду", "ок", "ага", "угу", "да", "нет"
]

# Проблемы для контекста
problems = [
    "заказ не пришёл", "товар сломался", "курьер опоздал", "деньги списали дважды",
    "не могу войти", "оплата не прошла", "заказ потерялся", "товар бракованный"
]

responses = [
    "Проверим", "Оформим возврат", "Извините", "Вернём деньги", "Восстановите",
    "Проверьте карту", "Ищем ваш заказ", "Заменим", "Доставим завтра"
]

# ============ КЛЮЧЕВЫЕ ДИАЛОГИ (ЯВНЫЕ ПРИМЕРЫ) ============
key_dialogs = []

# Позитивные (только восторг и похвала)
for word in positive_words:
    key_dialogs.append((f"Клиент: {word}! Оператор: Рады помочь.", "позитивный"))
    key_dialogs.append((f"Клиент: {word}, отличная работа! Оператор: Спасибо.", "позитивный"))

# Негативные (только претензии и ругань)
for word in negative_words:
    key_dialogs.append((f"Клиент: {word}, так нельзя! Оператор: Извините.", "негативный"))
    key_dialogs.append((f"Клиент: Это {word}, буду жаловаться. Оператор: Приносим извинения.", "негативный"))

# Нейтральные (спасибо, хорошо, окей — НЕ позитив)
for word in neutral_words:
    key_dialogs.append((f"Клиент: {word}. Оператор: Обращайтесь.", "нейтральный"))

# РЕАЛЬНЫЕ ДИАЛОГИ С ПРАВИЛЬНОЙ ЭМОЦИЕЙ
key_dialogs.extend([
    # ПОЗИТИВНЫЕ (восторг, похвала)
    ("Клиент: Заказ пришёл, всё отлично, вы супер! Оператор: Спасибо.", "позитивный"),
    ("Клиент: Какое прекрасное качество, восторг! Оператор: Рады помочь.", "позитивный"),
    ("Клиент: Супер, быстро доставили! Оператор: Спасибо.", "позитивный"),
    ("Клиент: Вы лучшие, спасибо большое! Оператор: Обращайтесь.", "позитивный"),
    ("Клиент: Великолепный сервис, очень доволен. Оператор: Спасибо за отзыв.", "позитивный"),
    ("Клиент: Вау, так быстро решили! Оператор: Рады помочь.", "позитивный"),
    
    # НЕГАТИВНЫЕ (претензии, ругань)
    ("Клиент: Курьер опоздал, это недопустимо! Оператор: Извините.", "негативный"),
    ("Клиент: Товар бракованный, возмутительно! Оператор: Оформим возврат.", "негативный"),
    ("Клиент: Ужасный сервис, никогда больше! Оператор: Приносим извинения.", "негативный"),
    ("Клиент: Кошмар, деньги списали дважды! Оператор: Вернём.", "негативный"),
    ("Клиент: Безобразие, курьер нагрубил. Оператор: Извините.", "негативный"),
    ("Клиент: Отвратительное качество, буду жаловаться. Оператор: Оформим возврат.", "негативный"),
    
    # НЕЙТРАЛЬНЫЕ (простое сообщение + спасибо/хорошо — НЕ позитив)
    ("Клиент: Заказ не пришёл, проверьте. Оператор: Проверим. Клиент: Спасибо.", "нейтральный"),
    ("Клиент: Товар сломался, оформите возврат. Оператор: Оформим. Клиент: Хорошо.", "нейтральный"),
    ("Клиент: Курьер опоздал, примите меры. Оператор: Примем. Клиент: Окей.", "нейтральный"),
    ("Клиент: Деньги списали дважды, верните. Оператор: Вернём. Клиент: Понял, жду.", "нейтральный"),
    ("Клиент: Помогите отменить заказ. Оператор: Сделаем. Клиент: Спасибо.", "нейтральный"),
    ("Клиент: Не могу войти в аккаунт. Оператор: Восстановите. Клиент: Попробую.", "нейтральный"),
    ("Клиент: Оплата прошла, но заказ не подтверждён. Оператор: Проверю. Клиент: Срочно.", "нейтральный"),
    ("Клиент: Товар пришёл повреждённый. Оператор: Оформим возврат. Клиент: Спасибо.", "нейтральный"),
    ("Клиент: Как поменять адрес. Оператор: В личном кабинете. Клиент: Нашёл.", "нейтральный"),
    ("Клиент: Доставка быстрая, курьер вежливый. Оператор: Спасибо. Клиент: 5 звёзд.", "позитивный"),  # похвала
    ("Клиент: Спасибо за быструю доставку. Оператор: Пожалуйста. Клиент: Отлично.", "позитивный"),
])

# ============ ВАРИАЦИИ ============
emotion_variations = {
    "позитивный": [
        "Клиент: {problem} Оператор: {response} Клиент: {word}!",
        "Клиент: {word}! {problem} решили. Оператор: Рады помочь.",
    ],
    "негативный": [
        "Клиент: {problem} Оператор: {response} Клиент: {word}!",
        "Клиент: {word}, {problem}! Оператор: Извините.",
    ],
    "нейтральный": [
        "Клиент: {problem} Оператор: {response} Клиент: {word}",
        "Клиент: {problem} Оператор: {response} Клиент: {word}",
    ]
}

# ============ ГЕНЕРАЦИЯ ============
all_dialogs = []

for dialog, emotion in key_dialogs:
    all_dialogs.append([dialog, emotion])

for emotion, templates in emotion_variations.items():
    current_count = len([d for d in all_dialogs if d[1] == emotion])
    
    if emotion == "позитивный":
        words = positive_words
    elif emotion == "негативный":
        words = negative_words
    else:
        words = neutral_words
    
    while current_count < 500:
        template = random.choice(templates)
        dialog = template.format(
            problem=random.choice(problems),
            response=random.choice(responses),
            word=random.choice(words)
        )
        all_dialogs.append([dialog, emotion])
        current_count += 1
    print(f"{emotion}: {current_count} диалогов")

random.shuffle(all_dialogs)

print(f"\nВсего: {len(all_dialogs)} диалогов")

with open("training_data_emotions.csv", "w", newline="", encoding="utf-8") as f:
    writer = csv.writer(f)
    writer.writerow(["dialog", "emotion"])
    writer.writerows(all_dialogs)

print("Сохранено в training_data_emotions.csv")